package com.github.kobalj.dataformater.params.impl;

import com.github.kobalj.dataformater.params.IParameterDataValues;

/**
 *
 * @author Jure Kobal
 */
public class CAddressDataValues implements IParameterDataValues {
    private String name;
    private String address;
    private String houseNumber;
    private String city;
    private int postNumber = -1;
    private String country;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getHouseNumber() {
        return houseNumber;
    }

    public void setHouseNumber(String houseNumber) {
        this.houseNumber = houseNumber;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getPostNumber() {
        return postNumber;
    }

    public void setPostNumber(int postNumber) {
        this.postNumber = postNumber;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
}
